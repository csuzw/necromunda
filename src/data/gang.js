/* TYPES */
type Weapon = string | {|
    name: string,
    wargear: Array<string>,
    ammo: Array<string>
|}

type Characteristic = {|
    base: number,
    modifier: number
|}

type Characteristics = {|
    movement: Characteristic,
    weaponSkill: Characteristic,
    ballisticSkill: Characteristic,
    strength: Characteristic,
    toughness: Characteristic,
    wounds: Characteristic,
    initiative: Characteristic,
    attacks: Characteristic,
    leadership: Characteristic,
    cool: Characteristic,
    willpower: Characteristic,
    intelligence: Characteristic,
|}

type Fighter = {|
    name: string,
    nickname?: string,
    type: string,
    archetype?: string,
    color?: string,
    image?: string,
    baseCost: number,
    experience: number,
    advancements: number,
    inRecovery: boolean,
    isDead: boolean,
    capturedBy: string,
    lastingInjuries: string,
    characteristics: Characteristics,
    weapons: Array<Weapon>,
    wargear: Array<string>,
    skills: Array<string>,
    rules: Array<string>
|}

type OutOfAction = {|
    me: number,
    opponent: number
|}

type Battle = {|
    territory: string,
    scenario: string,
    opponentName: string,
    opponentHouse: string,
    success: boolean,
    outOfAction: OutOfAction
|}

type Gang = {|
    name: string,
    house: string,
    affiliation?: string,
    reputation: number,
    territories: Array<string>,
    stash: {
        credits: number,
        weapons: Array<Weapon>,
        wargear: Array<string>
    },
    battles: Array<Battle>,
    fighters: Array<Fighter>
|}